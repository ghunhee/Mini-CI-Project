#!/bin/bash

echo "=========================================================="
echo "🔥 멀티스레드 Fork 데드락 및 Seccomp 안정성 스트레스 테스트 🔥"
echo "=========================================================="

echo "[1/2] 기존 빌드 찌꺼기 청소 및 최신 코드로 서버/클라이언트 재빌드..."
make clean > /dev/null 2>&1
make > /dev/null 2>&1

if [ $? -ne 0 ]; then
    echo "❌ 빌드 실패! 코드를 확인해주세요."
    exit 1
fi

echo "[2/2] 서버로 20개의 요청을 0.1초만에 동시에 발사합니다!"
echo "서버 터미널을 꼭 켜두고 [ RUNNING ] 상태가 겹치는 것을 관찰하세요."

# test_suite를 활용해 ncurses UI 없이 20개의 백그라운드 요청 폭격
for i in {1..20}; do
    ./test_suite > /dev/null 2>&1 &
done

# 모든 백그라운드 프로세스가 끝날 때까지 대기
wait

echo "=========================================================="
echo "✅ 20개의 동시 요청이 모두 끝났습니다!"
echo "만약 서버가 뻗거나 멈추지(Deadlock) 않고 모두 채점했다면,"
echo "우리가 수정한 _exit와 순수 시스템 콜 방어 로직이 완벽히 작동한 것입니다!"
echo "=========================================================="
